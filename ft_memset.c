#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned long int	i;
	char				*a;

	a = (char *)(b);
	i = 0;
	while (i < len)
	{
		*(a + i) = c;
		i++;
	}
	return (b);
}
